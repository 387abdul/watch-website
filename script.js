function displayTShirtCard(Mens) {
    const CardForMen = document.getElementById("mensCloths");

    const cards = Mens.map(shirt => {
        const card = document.createElement("div");
        card.className = "col-lg-3 col-md-6 col-sm-12";

        const img = document.createElement("img");
        img.src = shirt.image;

        const title = document.createElement("h3");
        title.textContent = shirt.title;

        const price = document.createElement("p");
        price.textContent = `$${shirt.price}`;

        const description = document.createElement("p");
        description.textContent = shirt.description;

        card.appendChild(img);
        card.appendChild(title);
        card.appendChild(price);
        card.appendChild(description);  

        return card;
    });

    cards.forEach(card => CardForMen.appendChild(card));
}

displayTShirtCard(Mens)